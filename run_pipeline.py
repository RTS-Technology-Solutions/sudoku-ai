# Puzzle generation configuration (generator.py)
GENERATE_DATASET = False  # Set to True to generate a dataset of puzzles with solutions
GEN_DATASET_SIZE = 1000000  # Number of puzzles to generate for the dataset
GEN_BLANKS = range(40,51)  # Range of blanks, keep under 64 (MIN to MAX+1)
GEN_MAX_SOLUTIONS = 10  # Maximum number of solutions allowed for each generated puzzle (1 for unique solution)
GEN_DATESTAMP = datetime.datetime.now().strftime("%Y%m%d%H%M")
GEN_DATASET_UID = f"{GEN_DATASET_SIZE}_{min(GEN_BLANKS)}-{max(GEN_BLANKS)}_{GEN_MAX_SOLUTIONS}_{GEN_DATESTAMP}"  # Unique identifier for the generated dataset based on parameters and timestamp

# Data preparation configuration (data_preparer.py)
PREPARE_DATASET = False  # Set to True to prepare the dataset (one-hot encoding and splitting)
TEST_MODE = False  # Set to True to run a test of the one-hot encoding function
PREP_DATASET_UID = "1000000_40-50_10_202606191736"  # Unique identifier for the prepared dataset ("1000_10-40_1_202606131802"), set to empty when GENERATE_DATASET = True
TARGET_RAW_DATASET = f"data/sudoku_puzzle_dataset_{GEN_DATASET_UID if GENERATE_DATASET else PREP_DATASET_UID}.json"  # Path to the dataset generated by generator.py
PERFORM_SPLIT = True  # Set to True to split the dataset into training and validation sets
TARGET_SPLIT_RATIO = 0.9  # Ratio for splitting the dataset into training and validation sets

# Training configuration and setup (trainer.py)
# Note: Ensure PREP_DATASET_UID is set to the target training dataset's UID if not generating a new dataset.
TRAIN_MODEL = True  # Set to True to train the model, False to skip training and use existing checkpoints
CHECKPOINT_PATH = "models/sudoku_model_checkpoint_latest.pth"
TARGET_TRAINING_DATASET = TARGET_RAW_DATASET.replace("data/sudoku_puzzle_dataset", "data/encoded/split/sudoku_puzzle_dataset_one_hot_encoded").replace(".json", "_train.json") # Path to the one-hot encoded training dataset generated by data_preparer.py
LEARNING_RATE = 0.001
ESTOP_PATIENCE = 20
TRAINING_EPOCHS = 200
SCHEDULER_FACTOR = 0.1  # Factor by which the learning rate will be reduced. new_lr = lr * factor
SCHEDULER_PATIENCE = 5  # Number of epochs with no improvement after which learning rate will be reduced.

# Testing configuration (tester.py)
# Note: Ensure PREP_DATASET_UID is set to the target test dataset's UID if not generating a new dataset.
TEST_MODEL = True  # Set to True to test the model, False to skip testing
HEADLESS_MODE = True  # Set to True to run without rendering (for faster testing), False to see the step-by-step visualization
TARGET_TEST_DATASET = TARGET_TRAINING_DATASET.replace("_train.json", "_test.json")  # Path to the one-hot encoded test dataset generated by data_preparer.py
TARGET_TEST_MODEL = "models/sudoku_model_best.pth"  # Path to the best trained model weights

