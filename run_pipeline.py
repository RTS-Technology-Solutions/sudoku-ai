def run_pipeline():
    # Step 1: Generate the dataset (if not already generated)
    if GENERATE_DATASET:
        print("Step 1: Generating dataset...")
        generator.main(GENERATE_DATASET, GEN_DATASET_SIZE, GEN_BLANKS, GEN_MAX_SOLUTIONS, GEN_DATESTAMP)
    else:
        print("Step 1: Skipping dataset generation (already generated)")

    # Step 2: Prepare the dataset (one-hot encoding and splitting)
    if PREPARE_DATASET:
        print("Step 2: Preparing dataset...")
        data_preparer.main(TEST_MODE, TARGET_RAW_DATASET, PERFORM_SPLIT, TARGET_SPLIT_RATIO)
    else:
        print("Step 2: Skipping dataset preparation (already prepared)")

    # Step 3: Train the model
    if TRAIN_MODEL:
        print("Step 3: Training model...")
        trainer.main(CHECKPOINT_PATH, TARGET_TRAINING_DATASET, LEARNING_RATE, ESTOP_PATIENCE, TRAINING_EPOCHS, SCHEDULER_FACTOR, SCHEDULER_PATIENCE)  # This will train the model and save checkpoints
    else:
        print("Step 3: Skipping model training (using existing checkpoints)")

    # Step 4: Test the model
    if TEST_MODEL:    
        print("Step 4: Testing model...")
        tester.main(HEADLESS_MODE, TARGET_TEST_DATASET, TARGET_TEST_MODEL)
    else:
        print("Step 4: Skipping model testing")

def main():
    # Allow globals modification
    global GENERATE_DATASET, PREPARE_DATASET, TRAIN_MODEL, TEST_MODEL

    # Additional prechecks will be added here
    # Checks must be ordered from last step to first to ensure hierarchy of dependencies.
    
    # Prevent overwriting existing datasets.
    if PREPARE_DATASET and os.path.exists(TARGET_TRAINING_DATASET):
        # Don't overwrite a prepared dataset.
        print(f"Warning: Prepared training dataset already exists at {TARGET_TRAINING_DATASET}. Skipping preparation to avoid overwriting.")
        PREPARE_DATASET = False  # Set to False to skip preparation since the prepared dataset already exists

    if GENERATE_DATASET and os.path.exists(TARGET_RAW_DATASET):
        # Don't overwrite a raw dataset.
        print(f"Warning: Raw dataset already exists at {TARGET_RAW_DATASET}. Skipping generation to avoid overwriting.")
        GENERATE_DATASET = False  # Set to False to skip generation since the dataset already exists
    
    # Prevent moving forward without required datasets.
    if TEST_MODEL and not os.path.exists(TARGET_TEST_MODEL) and not TRAIN_MODEL:
        # No model checkpoint to test with.
        print(f"Warning: Test model checkpoint not found at {TARGET_TEST_MODEL}. Enabling training to create it.")
        TRAIN_MODEL = True  # Set to True to enable training since the test model checkpoint doesn't exist and is needed for testing

    if TRAIN_MODEL and not os.path.exists(TARGET_TRAINING_DATASET) and not PREPARE_DATASET:
        # No training dataset to train from.
        print(f"Warning: Prepared training dataset not found at {TARGET_TRAINING_DATASET}. Enabling dataset preparation to create it.")
        PREPARE_DATASET = True  # Set to True to enable preparation since the training dataset is needed for training and doesn't exist
    
    if PREPARE_DATASET and not os.path.exists(TARGET_RAW_DATASET) and not GENERATE_DATASET:
        # No raw dataset to prepare from.
        print(f"Warning: Raw dataset not found at {TARGET_RAW_DATASET}. Enabling dataset generation to create it.")
        GENERATE_DATASET = True  # Set to True to enable generation since the raw dataset is needed for preparation and doesn't exist
    

    run_pipeline()

if __name__ == "__main__":
    main()