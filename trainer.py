class ParallelLineConv(nn.Module):
    """
    Scans a 6x6 spatial region but zeros out the center cells.
    Forces the model to process two distinct, parallel vertical lines
    simultaneously, exchanging information across empty spaces.
    """
    def __init__(self, in_channels, out_channels):
        super(ParallelLineConv, self).__init__()
        self.conv = nn.Conv2d(in_channels, out_channels, kernel_size=6, padding='same')
        
        # Build the exact 6x6 non-contiguous binary mask
        mask = torch.zeros(6, 6)
        mask[:, 0] = 1.0  # Line 1: Leftmost vertical column
        mask[:, 2] = 1.0  # Line 2: Vertically parallel line spaced 1 cell away
        
        # Register as a buffer so it automatically handles .to(device) calls
        self.register_buffer('mask', mask.unsqueeze(0).unsqueeze(0)) # Shape: (1, 1, 6, 6)

    def forward(self, x):
        # Apply the static spatial mask to the weights before convolving
        with torch.no_grad():
            self.conv.weight.mul_(self.mask)
        return self.conv(x)

class SnakePathConv(nn.Module):
    """
    Scans an asymmetric 6x4 bounding box following a coordinate path matrix.
    Forces feature maps to explicitly track sequential dependency chains.
    """
    def __init__(self, in_channels, out_channels):
        super(SnakePathConv, self).__init__()
        self.conv = nn.Conv2d(in_channels, out_channels, kernel_size=(6, 4), padding='same')
        
        # Build custom spatial sequence path mask
        mask = torch.zeros(6, 4)
        path = [(0,0), (1,0), (2,0), (2,1), (2,2), (2,3), (3,3), (4,3), (5,3)]
        for r, c in path:
            mask[r, c] = 1.0
            
        self.register_buffer('mask', mask.unsqueeze(0).unsqueeze(0)) # Shape: (1, 1, 6, 4)

    def forward(self, x):
        with torch.no_grad():
            self.conv.weight.mul_(self.mask)
        return self.conv(x)
    
class SudokuDataset(Dataset):
    # Custom dataset wrapper to load packed tensors
    def __init__(self, puzzles, solutions):
        self.puzzles = puzzles
        self.solutions = solutions

    def __len__(self):
        return len(self.puzzles)

    def __getitem__(self, idx):
        # Memory Hack: Store data as uint8 (1 byte) and convert to float (4 bytes) only for the specific batch
        puzzle = self.puzzles[idx].float()
        solution = self.solutions[idx].long()
        
        # Rearrange the data from (Height, width, Channels) to (Channels, Height, Width) as required by PyTorch's convolutional layers
        puzzle = puzzle.permute(2, 0, 1)  # Convert from (9, 9, 10) to (10, 9, 9) **Updated: 2026-06-12** to match the expected input shape for Conv2d
        
        # Collapse the one-hot dimension to integer labels (0-9) FIRST while it's still (9, 9, 10)
        solution_int = torch.argmax(solution, dim=-1).long() # Shape: (9, 9)

        return puzzle, solution_int

def load_and_pack_dataset(file_path):
    """
    Streams a large JSON dataset and packs it into compact uint8 tensors to save RAM.
    1,000,000 records approx = 1.6 GB in RAM using this method vs 50GB using standard JSON loading.
    """
    p_temp = []
    s_temp = []
    all_p = []
    all_s = []
    
    print(f"Streaming {file_path} into memory...")
    with open(file_path, 'rb') as f:
        for i, item in enumerate(ijson.items(f, 'item')):
            # Compress 10-channel one-hot into uint8
            p_temp.append(torch.tensor(item['puzzle'], dtype=torch.uint8).unsqueeze(0))
            s_temp.append(torch.tensor(item['solution'], dtype=torch.uint8).unsqueeze(0))
            
            # Periodically consolidate to prevent list fragmentation
            if (i + 1) % 10000 == 0:
                all_p.append(torch.cat(p_temp, dim=0))
                all_s.append(torch.cat(s_temp, dim=0))
                p_temp = []
                s_temp = []
                if (i + 1) % 100000 == 0:
                    print(f"  Loaded {i+1} puzzles...")
        
        # Catch the remainder
        if p_temp:
            all_p.append(torch.cat(p_temp, dim=0))
            all_s.append(torch.cat(s_temp, dim=0))
            
    return torch.cat(all_p, dim=0), torch.cat(all_s, dim=0)

class BlackBoxBreakerCNN(nn.Module):
    # The CNN architecture as described above using PyTorch's nn.Conv2d for convolutional layers
    def __init__(self):
        super(BlackBoxBreakerCNN, self).__init__()
        
        # --- LAYER 1: MULTI-SCALE GEOMETRIC COVERAGE ---
        # Input: 10 channels (one-hot tokens)
        # Output: 16 filters * 6 shapes = 96 channels
        # NOTE: padding='same' with even kernel sizes (2, 4, 6) triggers a PyTorch warning about asymmetric padding.
        # This is expected behavior and doesn't affect functionality—the model handles it correctly internally.
        self.conv2x2 = nn.Conv2d(10, 16, kernel_size=2, padding='same')
        self.conv3x3 = nn.Conv2d(10, 16, kernel_size=3, padding='same')
        self.conv4x4 = nn.Conv2d(10, 16, kernel_size=4, padding='same')
        self.conv2x6 = nn.Conv2d(10, 16, kernel_size=(2, 6), padding='same')
        self.conv1x5 = nn.Conv2d(10, 16, kernel_size=(1, 5), padding='same')
        self.conv5x1 = nn.Conv2d(10, 16, kernel_size=(5, 1), padding='same')

        # Rule specific lenses to capture actual spatial relationships (32 channels total)
        self.conv1x9 = nn.Conv2d(10, 16, kernel_size=(1, 9), padding='same')  # Scans entire rows to capture row constraints
        self.conv9x1 = nn.Conv2d(10, 16, kernel_size=(9, 1), padding='same')  # Scans entire columns to capture column constraints
        
        # Non-contiguous domain specific lens (16 channels total)
        self.conv3x3_strided = nn.Conv2d(10, 16, kernel_size=3, stride=3, padding=0)  # Scans non-overlapping 3x3 blocks to capture box constraints

        # 2 Non-Contiguous Domain-Specific Lenses - 16 filters each, 32 channels (Added: 2026-06-15)
        self.parallel_lens = ParallelLineConv(10, 16)  # Scans two parallel vertical lines in a 6x6 region
        self.snake_lens = SnakePathConv(10, 16)  # Scans a custom snake-like path in a 6x4 region

        # Feature Regularizer (Added: 2026-06-12)
        self.dropout = nn.Dropout2d(p=0.1)  # Randomly zero out entire channels with a probability to encourage more robust feature learning and prevent overfitting.
        
        # --- LAYER 2: THE SYNTHESIZER ---
        # Input: 128 channels from Layer 1
        # Output: 32 compressed channels
        self.layer2 = nn.Conv2d(176, 32, kernel_size=3, padding='same')
        
        # --- FINAL LAYER: THE PREDICTOR ---
        # Input: 32 compressed channels
        # Output: 10 channels (Raw scores for digits 0-9)
        self.final_layer = nn.Conv2d(32, 10, kernel_size=1)

    def forward(self, x):
        # 1. Run all 8 custom shape lenses in parallel
        out2x2 = F.relu(self.conv2x2(x))
        out3x3 = F.relu(self.conv3x3(x))
        out4x4 = F.relu(self.conv4x4(x))
        out2x6 = F.relu(self.conv2x6(x))
        out1x5 = F.relu(self.conv1x5(x))
        out5x1 = F.relu(self.conv5x1(x))
        
         # 2. Run row and column global constraint lenses
        out1x9 = F.relu(self.conv1x9(x))
        out9x1 = F.relu(self.conv9x1(x))
        
        # 3. Run strided block lens & broadcast back to 9x9 space
        # out3x3_strided_raw has spatial shape (B, 16, 3, 3)
        out3x3_strided_raw = F.relu(self.conv3x3_strided(x))
        # Upsample (3, 3) -> (9, 9) to broadcast block-level characteristics back to cell coordinates
        out3x3_strided = F.interpolate(out3x3_strided_raw, size=(9, 9), mode='nearest')
        
        # 4. Run non-contiguous structural lenses
        out_parallel = F.relu(self.parallel_lens(x))  # Output from the parallel line lens
        out_snake = F.relu(self.snake_lens(x))  # Output from the snake path lens
        
        # 2. Glue the 96 perspectives together along the channel axis
        x1 = torch.cat([
            out2x2, out3x3, out4x4, out2x6, out1x5, out5x1, 
            out1x9, out9x1, out3x3_strided,
            out_parallel, out_snake        
        ], dim=1) # Shape: (B, 176, 9, 9) - 176 channels from all the lenses combined
        
        # Apply the dropout to the combined feature maps (Added: 2026-06-12)
        x1_regularized = self.dropout(x1)

        # 3. Compress and synthesize down to 32 channels
        x2 = F.relu(self.layer2(x1_regularized)) # use the regularized features
        
        # 4. Output the raw scores for the entire 9x9x10 grid
        logits = self.final_layer(x2)
        return logits

def setup_logging(target_training_dataset):
    """
    Sets up logging for the training session.
    Extracts dataset name and creates a log file matching the test log naming convention.
    Returns the file handle for writing log entries.
    """
    # Create logs directory if it doesn't exist
    log_dir = "analysis/logs"
    if not os.path.exists(log_dir):
        os.makedirs(log_dir)
    
    # Extract dataset name from path
    dataset_basename = os.path.basename(target_training_dataset)
    # Remove _train.json suffix to get base name
    dataset_name = dataset_basename.replace("_train.json", "")
    
    # Create log filename matching test log convention
    log_filename = os.path.join(log_dir, f"results_{dataset_name}_train.log")
    
    return log_filename

def log_message(log_file, message):
    """
    Writes a message to both stdout and the log file.
    """
    print(message)
    with open(log_file, 'a') as f:
        f.write(message + "\n")

def main(checkpoint_path=CHECKPOINT_PATH, target_training_dataset=TARGET_TRAINING_DATASET, learning_rate=LEARNING_RATE, estop_patience=ESTOP_PATIENCE, training_epochs=TRAINING_EPOCHS, scheduler_factor=SCHEDULER_FACTOR, scheduler_patience=SCHEDULER_PATIENCE, headless_mode_active=HEADLESS_MODE):
    # Setup logging
    log_file = setup_logging(target_training_dataset)
    training_start_time = time.time()
    
    log_message(log_file, "=" * 60)
    log_message(log_file, f"Training Session Started: {datetime.datetime.now().strftime('%Y%m%d_%H%M%S')}")
    log_message(log_file, "=" * 60)
    log_message(log_file, "")
    
    # GPU OR CPU? 
    # For this task, GPU is highly recommended if available due to the amount of parallel matrix math involved in CNNs.
    device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
    log_message(log_file, f"Running training on: {device}")

    # Load the one-hot encoded dataset using the new streaming packer to save memory
    train_p, train_s = load_and_pack_dataset(target_training_dataset)
    log_message(log_file, f"Loaded training dataset with {len(train_p)} puzzles.")
    
    val_p, val_s = load_and_pack_dataset(target_training_dataset.replace("_train.json", "_val.json"))
    log_message(log_file, f"Loaded validation dataset with {len(val_p)} puzzles.")

    # Create the Dataset instances
    train_dataset = SudokuDataset(train_p, train_s)
    val_dataset = SudokuDataset(val_p, val_s)

    # Create the DataLoaders to Handle the batching strategy
    train_loader = DataLoader(train_dataset, batch_size=128, shuffle=True) # Increased batch size for better GPU utilization
    val_loader = DataLoader(val_dataset, batch_size=128, shuffle=False)

    # Instantiating the tools
    model = BlackBoxBreakerCNN().to(device)
    criterion = nn.CrossEntropyLoss()
    optimizer = torch.optim.Adam(model.parameters(), lr=learning_rate)
    log_message(log_file, f"Loading {type(model).__name__} model, {type(criterion).__name__} loss function, and {type(optimizer).__name__} optimizer.")
    
    # Learning Rate Scheduler setup
    scheduler = optim.lr_scheduler.ReduceLROnPlateau(
        optimizer,
        mode='min',
        factor=scheduler_factor,
        patience=scheduler_patience
    )

    # Live Plotting Setup
    if not headless_mode_active:
        plt.ion()
        fig, ax = plt.subplots(figsize=(8, 5))

    # Training init variables    
    train_losses = []
    val_losses = []
    best_val_loss = float('inf')
    start_epoch = 0
    total_epochs = 1  # Placeholder, will be updated based on checkpoint loading
    epochs_without_improvement = 0

    if os.path.exists(checkpoint_path):
        log_message(log_file, f"Found existing checkpoint at {checkpoint_path}. Restoring session...")
        checkpoint = torch.load(checkpoint_path, map_location=device)
        
        # Restore components in strict order
        model.load_state_dict(checkpoint['model_state_dict'])
        optimizer.load_state_dict(checkpoint['optimizer_state_dict'])
        scheduler.load_state_dict(checkpoint['scheduler_state_dict'])
        
        start_epoch = checkpoint['epoch']
        total_epochs = start_epoch + training_epochs  # Continue training for the remaining epochs
        best_val_loss = checkpoint['best_val_loss']
        train_losses = checkpoint['train_losses']
        val_losses = checkpoint['val_losses']
        log_message(log_file, f"Training will resume from Epoch {start_epoch}. Best Val Loss so far: {best_val_loss:.4f}")
    else:
        total_epochs = training_epochs  # Start fresh training run
        log_message(log_file, "No checkpoint found. Starting fresh training run.")

    log_message(log_file, f"Initializing {"continued" if start_epoch > 0 else "new"} training for {training_epochs} epochs with early stopping patience of {estop_patience} epochs.")
    log_message(log_file, "\nStarting training loop...")

    # Training the model by iterating through the DataLoader
    for epoch in range(start_epoch, total_epochs):
        model.train()
        running_train_loss = 0.0

        for puzzles, solutions in train_loader:
            puzzles, solutions = puzzles.to(device), solutions.to(device)
            
            # Step 1: Clear out old gradients
            optimizer.zero_grad()

            # Step 2: Forward pass
            outputs = model(puzzles)
            
            # Step 3: Calculate error
            loss = criterion(outputs, solutions)

            # Step 4: Backpropagation
            loss.backward()

            # Step 5: Optimizer step
            optimizer.step()

            # Accumulate reporting loss
            running_train_loss += loss.item() * puzzles.size(0)

        # Average training loss
        epoch_train_loss = running_train_loss / len(train_loader.dataset)

        # Validation
        model.eval()
        running_val_loss = 0.0

        with torch.no_grad():
            for val_puzzles, val_solutions in val_loader:
                val_puzzles, val_solutions = val_puzzles.to(device), val_solutions.to(device)
                val_outputs = model(val_puzzles)
                val_loss = criterion(val_outputs, val_solutions)
                running_val_loss += val_loss.item() * val_puzzles.size(0)
            
        epoch_val_loss = running_val_loss / len(val_loader.dataset)

        # Step the learning rate scheduler
        scheduler.step(epoch_val_loss)

        epoch_log = f"Epoch {epoch+1}/{training_epochs} - Train Loss: {epoch_train_loss:.4f} - Val Loss: {epoch_val_loss:.4f}"
        log_message(log_file, epoch_log)

        # Save the model checkpoint for this epoch
        checkpoint = {
            'epoch': epoch + 1,
            'model_state_dict': model.state_dict(),
            'optimizer_state_dict': optimizer.state_dict(),
            'scheduler_state_dict': scheduler.state_dict(),
            'best_val_loss': best_val_loss,
            'train_losses': train_losses,
            'val_losses': val_losses
        }
        torch.save(checkpoint, f"models/sudoku_model_checkpoint_{epoch+1}.pth") # Save an epoch checkpoint for posterity
        torch.save(checkpoint, "models/sudoku_model_checkpoint_latest.pth")  # Overwrite the latest checkpoint for reference

        # Add the values to the reporting data
        train_losses.append(epoch_train_loss)
        val_losses.append(epoch_val_loss)
        
        # Update the live plot
        if not headless_mode_active:
            ax.clear()
            ax.plot(range(1, len(train_losses) + 1), train_losses, label='Train Loss', color='blue', linewidth=2)
            ax.plot(range(1, len(val_losses) + 1), val_losses, label='Val Loss', color='orange', linewidth=2)
            ax.set_title('Sudoku AI Training Progression')
            ax.set_xlabel('Epoch')
            ax.set_ylabel('Loss Value')
            ax.legend()
            ax.grid(True, linestyle='--', alpha=0.6)
            
            # Update the display
            plt.draw()
            plt.pause(0.1)  # Brief pause to let GUI update

        # Early stop check
        if epoch_val_loss < best_val_loss:
            best_val_loss = epoch_val_loss
            epochs_without_improvement = 0
            # Save the best model state
            torch.save(model.state_dict(), "models/sudoku_model_best.pth")
        else:
            epochs_without_improvement += 1
            if epochs_without_improvement >= estop_patience:
                early_stop_msg = f"Early stopping triggered at epoch {epoch+1}. Best Val Loss: {best_val_loss:.4f}"
                log_message(log_file, early_stop_msg)
                break

    # --- FINAL REPORT GENERATION ---
    training_end_time = time.time()
    training_duration = training_end_time - training_start_time
    
    log_message(log_file, "")
    log_message(log_file, "=" * 60)
    log_message(log_file, "TRAINING SUMMARY")
    log_message(log_file, "=" * 60)
    log_message(log_file, f"Total Epochs Completed: {len(train_losses)}")
    log_message(log_file, f"Best Validation Loss: {best_val_loss:.6f}")
    log_message(log_file, f"Final Training Loss: {train_losses[-1]:.6f}")
    log_message(log_file, f"Final Validation Loss: {val_losses[-1]:.6f}")
    log_message(log_file, f"Training Duration: {training_duration / 60:.2f} minutes")
    log_message(log_file, f"Training completed at: {datetime.datetime.now().strftime('%Y%m%d_%H%M%S')}")
    log_message(log_file, "=" * 60)
    
    print("\nGenerating final training report...")
    
    # 1. Create a fresh, non-interactive figure
    plt.figure(figsize=(10, 6))
    
    # 2. Plot the data we collected in memory during the loop
    plt.plot(range(1, len(train_losses) + 1), train_losses, label='Train Loss', color='blue', linewidth=2)
    plt.plot(range(1, len(val_losses) + 1), val_losses, label='Val Loss', color='orange', linewidth=2)
    
    # 3. Apply all formatting (labels, titles, grid)
    plt.title('Sudoku AI Training Progression (Headless Run)')
    plt.xlabel('Epoch')
    plt.ylabel('Loss Value (Cross-Entropy)')
    plt.legend()
    plt.grid(True, linestyle='--', alpha=0.6)
    
    # 4. Save the finalized image
    datetimestamp = datetime.datetime.now().strftime("%Y%m%d_%H%M")
    figure_filename = os.path.join("analysis","training_runs",os.path.basename(target_training_dataset)).replace("sudoku_puzzle_dataset_one_hot_encoded", "training_run").replace("_train.json", f"_{datetimestamp}.png")
    
    # Create directory if it doesn't exist
    os.makedirs(os.path.dirname(figure_filename), exist_ok=True)
    
    plt.savefig(figure_filename)  # Save the final training progress graph
    log_message(log_file, f"\nFinal training progression graph saved as {figure_filename}")
    log_message(log_file, "Training complete. Best model saved as sudoku_model_best.pth")

if __name__ == "__main__":
    main()